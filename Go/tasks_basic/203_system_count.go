package main

import "fmt"

func main() {
    // здуесь разные системы счисления
    // 100 - десятичныя
    // 010 - восьмиричная
    sum := 100 + 010
    fmt.Println(sum)
}

/*

Целочисленные литералы
Двоичная --- префикс 0b или 0В (пример 0b100)
Восьмиричная --- префикс 0 или 0о (пример 010)
Шестнадцатиричная --- префикс 0х или 0Х (например 0хF)
Мнимые --- ceaabrc i (например 3i)

Можго использовать _ в качестве разделителя
для удобства чтения 1_000_000


*/
